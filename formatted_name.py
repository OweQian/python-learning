def get_formatted_name(first, last, middle = ''):
  if middle:
    full_name = f"{first} {middle} {last}"
  else:
    full_name = f"{first} {last}"
  return full_name.title()

musician = get_formatted_name('jimi', 'hendrix')
print(musician)

musician = get_formatted_name('john', 'hooker', 'lee')
print(musician)